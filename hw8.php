<?php
// Получаем текущий месяц (от 1 до 12)
$month = date('n'); // например, июнь будет 6

// Определение сезона
if ($month >= 3 && $month <= 5) {
    $season = "Весна";
} elseif ($month >= 6 && $month <= 8) {
    $season = "Лето";
} elseif ($month >= 9 && $month <= 11) {
    $season = "Осень";
} else {
    // Декабрь (12), Январь (1), Февраль (2)
    $season = "Зима";
}

// Вывод сообщения
echo "Сейчас $season.";
?>
